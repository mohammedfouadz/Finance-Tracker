interface I18nContextType {
  lang: string;
  setLang: (lang: string) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
  isRtl: boolean;
  formatDate: (date: Date | string, style?: "long" | "short" | "weekday") => string;
  formatNumber: (value: number, arabicDigits?: boolean) => string;
}

const I18nContext = createContext<I18nContextType>({
  lang: "en",
  setLang: () => {},
  t: (key) => key,
  isRtl: false,
  formatDate: (date) => String(date),
  formatNumber: (value) => String(value),
});

export function I18nProvider({ children, initialLang = "en" }: { children: ReactNode; initialLang?: string }) {
  const [lang, setLang] = useState(initialLang);
  const isRtl = lang === "ar";

  useEffect(() => {
    document.documentElement.dir = isRtl ? "rtl" : "ltr";
    document.documentElement.lang = lang;
    // Add a class for any language-specific CSS overrides
    document.documentElement.classList.toggle("lang-ar", lang === "ar");
    document.documentElement.classList.toggle("lang-en", lang === "en");
  }, [lang, isRtl]);

  /**
   * Lookup a translation key with optional interpolation.
   *   t("dashboard.activeDebts", { count: 5 })  →  "5 ديون نشطة"
   * Falls back to English, then to the raw key.
   */
  const t = (key: string, vars?: Record<string, string | number>): string => {
    let value = translations[lang]?.[key] ?? translations.en[key] ?? key;
    if (vars) {
      Object.entries(vars).forEach(([k, v]) => {
        value = value.replace(new RegExp(`{${k}}`, "g"), String(v));
      });
    }
    return value;
  };

  const formatDateLocal = (date: Date | string, style: "long" | "short" | "weekday" = "long") =>
    formatDate(date, lang, style);

  const formatNumberLocal = (value: number, arabicDigits = false) =>
    formatNumber(value, lang, arabicDigits);

  return (
    <I18nContext.Provider value={{
      lang,
      setLang,
      t,
      isRtl,
      formatDate: formatDateLocal,
      formatNumber: formatNumberLocal,
    }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}
