const ARABIC_MONTHS_LONG = [
  "يناير", "فبراير", "مارس", "أبريل", "مايو", "يونيو",
  "يوليو", "أغسطس", "سبتمبر", "أكتوبر", "نوفمبر", "ديسمبر"
];

const ARABIC_MONTHS_SHORT = [
  "ينا", "فبر", "مار", "أبر", "ماي", "يون",
  "يول", "أغس", "سبت", "أكت", "نوف", "ديس"
];

const ARABIC_DAYS = [
  "الأحد", "الإثنين", "الثلاثاء", "الأربعاء", "الخميس", "الجمعة", "السبت"
];

/** Format a date according to the active language. */
export function formatDate(date: Date | string, lang: string, style: "long" | "short" | "weekday" = "long"): string {
  const d = typeof date === "string" ? new Date(date) : date;
  if (isNaN(d.getTime())) return "";

  if (lang === "ar") {
    const day = d.getDate();
    const month = style === "short" ? ARABIC_MONTHS_SHORT[d.getMonth()] : ARABIC_MONTHS_LONG[d.getMonth()];
    const year = d.getFullYear();
    if (style === "weekday") {
      return `${ARABIC_DAYS[d.getDay()]}، ${day} ${month} ${year}`;
    }
    return `${day} ${month} ${year}`;
  }

  // English fallback
  const opts: Intl.DateTimeFormatOptions =
    style === "weekday"
      ? { weekday: "long", year: "numeric", month: "long", day: "numeric" }
      : style === "short"
      ? { year: "numeric", month: "short", day: "numeric" }
      : { year: "numeric", month: "long", day: "numeric" };
  return d.toLocaleDateString("en-US", opts);
}

/** Convert Latin digits to Arabic-Indic digits (٠١٢٣٤٥٦٧٨٩). */
export function toArabicDigits(input: string | number): string {
  return String(input).replace(/[0-9]/g, (d) => "٠١٢٣٤٥٦٧٨٩"[Number(d)]);
}

/** Format a number with locale-aware separators. Set arabicDigits=true for ٠١٢٣ digits. */
export function formatNumber(value: number, lang: string, arabicDigits = false): string {
  const formatted = value.toLocaleString(lang === "ar" ? "ar-EG" : "en-US");
  return arabicDigits && lang === "ar" ? toArabicDigits(formatted) : formatted;
}

